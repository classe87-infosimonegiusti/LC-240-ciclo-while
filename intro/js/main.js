console.log('Stampo i numero da 0 a 100 con un ciclo for');

for (let i = 0; i <= 100; i++) {
    console.log(i);
}


console.log('Stampo i numero da 0 a 100 con un ciclo while');

let c = 0;
while ( c <= 100 ) {
    console.log(c);
    c++;
}

